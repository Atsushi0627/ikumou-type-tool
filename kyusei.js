/**
 * kyusei.js - 九星本命星算出
 *
 * 立春は2/4固定の簡易版。境界日（2/3〜2/5生まれ）は要注意。
 * 将来的には年別立春テーブルへの差し替えを推奨。
 */

/**
 * 九星本命星を算出する
 * @param {number} year  西暦年
 * @param {number} month 月(1-12)
 * @param {number} day   日(1-31)
 * @returns {{ star: number, name: string }}
 */
export function getHonmeiStar(year, month, day) {
  // 立春(簡易的に2/4)より前の誕生日は前年扱い
  // ※厳密には立春は年により2/3〜2/5で揺れるが、本ツールは2/4固定の簡易版とする
  //   （正確性を期す場合は将来、年別立春テーブルを持たせる）
  let y = year;
  if (month === 1 || (month === 2 && day <= 3)) {
    y -= 1;
  }

  // 西暦各桁の和を1桁になるまで足す
  let s = String(y).split('').reduce((a, b) => a + Number(b), 0);
  while (s >= 10) {
    s = String(s).split('').reduce((a, b) => a + Number(b), 0);
  }

  // 11から引く
  let star = 11 - s;
  if (star >= 10) star -= 9;
  if (star === 0) star = 9;

  const names = {
    1: '一白水星', 2: '二黒土星', 3: '三碧木星', 4: '四緑木星', 5: '五黄土星',
    6: '六白金星', 7: '七赤金星', 8: '八白土星', 9: '九紫火星'
  };

  return { star, name: names[star] };
}

/**
 * 本命星 → 2軸スコア
 * X軸: 判断の動き方  -2(理屈・論理) 〜 +2(感情・直感)
 * Y軸: 決断スピード  -2(じっくり)  〜 +2(即決)
 *
 * ※この対応は接客設計用の整理であり、占術として確立されたものではない。
 *   施術者の経験で調整する場合はこのオブジェクトを直接編集してよい。
 */
export const KYUSEI_AXIS = {
  1: { x: -1, y: -2 }, // 一白水星 柔軟・思索・慎重・冷静
  2: { x: +1, y: -2 }, // 二黒土星 堅実・温厚・忍耐・調和
  3: { x: +1, y: +2 }, // 三碧木星 好奇心・勢い・行動的
  4: { x: +1, y: -1 }, // 四緑木星 社交・協調・人当たり
  5: { x: -1, y: +2 }, // 五黄土星 統率・積極・自分で決める
  6: { x: -2, y:  0 }, // 六白金星 理論・正義・プライド
  7: { x: +2, y: +1 }, // 七赤金星 楽しさ・社交・軽快
  8: { x: -1, y: -1 }, // 八白土星 信念・真面目・粘り
  9: { x: +2, y: +2 }  // 九紫火星 直感・情熱・美的・即断
};
