/**
 * うるう年判定（3月生まれに+1補正が必要）
 * @param {number} year
 * @returns {boolean}
 */
export function isLeap(year) {
  return (year % 4 === 0 && year % 100 !== 0) || (year % 400 === 0);
}

/**
 * 早見表の年キーを取得（1910-1961の範囲に52年周期でマッピング）
 * @param {number} year
 * @returns {number}
 */
function tableYearKey(year) {
  let y = year;
  while (y > 1961) y -= 52;
  while (y < 1910) y += 52;
  return y;
}

/**
 * KIN番号を算出する
 * @param {number} year
 * @param {number} month
 * @param {number} day
 * @returns {{ kin: number, sunSeal: { no: number, name: string }, tone: number }}
 */
export function getKin(year, month, day) {
  const key = tableYearKey(year);
  const base = MAYA_TABLE[key][month - 1];
  let kin = base + day;

  // うるう年の3月は+1補正
  if (isLeap(year) && month === 3) kin += 1;

  // 260で正規化（0は260扱い）
  kin = ((kin - 1) % 260) + 1;
  if (kin <= 0) kin += 260;

  // 太陽の紋章 (1-20)
  let sealNo = kin % 20;
  if (sealNo === 0) sealNo = 20;

  // 銀河の音 (1-13)
  let tone = kin % 13;
  if (tone === 0) tone = 13;

  const seals = {
    1:  '赤い龍',
    2:  '白い風',
    3:  '青い夜',
    4:  '黄色い種',
    5:  '赤い蛇',
    6:  '白い世界の橋渡し',
    7:  '青い手',
    8:  '黄色い星',
    9:  '赤い月',
    10: '白い犬',
    11: '青い猿',
    12: '黄色い人',
    13: '赤い空歩く人',
    14: '白い魔法使い',
    15: '青い鷲',
    16: '黄色い戦士',
    17: '赤い地球',
    18: '白い鏡',
    19: '青い嵐',
    20: '黄色い太陽'
  };

  return { kin, sunSeal: { no: sealNo, name: seals[sealNo] }, tone };
}

